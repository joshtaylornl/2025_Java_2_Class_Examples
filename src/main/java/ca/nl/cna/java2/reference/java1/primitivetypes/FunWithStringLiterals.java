package ca.nl.cna.java2.reference.java1.primitivetypes;

public class FunWithStringLiterals {

    public static void main(String[] args) {
        System.out.println("Fun with String Literals");

        String basicString = "This is a String literal!";

        System.out.println("\n" + basicString);

        int i = 12;

        System.out.println("\n" + basicString + i);

        System.out.println("\n" + basicString);
    }

}
